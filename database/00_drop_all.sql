-- =============================================================================
-- 文件名：00_drop_all.sql
-- 作用：清理 qbank_db 数据库内本项目创建的所有对象，便于开发阶段反复重建
-- 适用数据库：qbank_db
-- 注意事项：
--   - 执行后将删除 qbank schema 下的所有对象（表、视图、函数、存储过程、触发器等）
--   - 本脚本不会删除 qbank_db 数据库本身
--   - 不会删除系统数据库、系统对象或 omm 用户
-- =============================================================================

\echo '[INFO] 开始清理 qbank_db 内的项目数据库对象...'

-- -----------------------------------------------------------------------------
-- 删除项目 schema（核心清理逻辑）
-- 本项目后续所有正式表、视图、函数、存储过程、触发器均放在 qbank schema 下
-- 使用 CASCADE 自动删除该 schema 下的所有依赖对象
-- -----------------------------------------------------------------------------
DROP SCHEMA IF EXISTS qbank CASCADE;

-- -----------------------------------------------------------------------------
-- 清理早期测试表
-- 以下为项目早期连接测试阶段在 public schema 下创建的临时测试表
-- 正式项目对象不会放在 public schema 中
-- -----------------------------------------------------------------------------
DROP TABLE IF EXISTS public.course CASCADE;
DROP TABLE IF EXISTS public.question_type CASCADE;

-- 说明：用户角色（如 qbank_app、qbank_readonly）的清理与重建
--       将在 09_init_roles.sql 中统一处理，本脚本不删除用户角色

\echo '[DONE] 项目数据库对象清理完成。'
